package basic.programming;

public class IfElseIfExample {

    public static void main(String[] args) {
        int num = -12;
        if (num == 0) {
            System.out.println("num is zero");
        } else if (num > 0) {
            System.out.println("num is positive");
        } else {
            System.out.println("num is nagative");
        }

    }
}
